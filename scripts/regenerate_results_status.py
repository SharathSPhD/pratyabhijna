#!/usr/bin/env python
"""Regenerate benchmarks/results_v0.4/STATUS.md from actual JSON results.

v0.4.1 review fix #4. The committed STATUS.md was a hand-edited
snapshot whose row counts disagreed with the underlying domain JSON
files (e.g. it claimed `complete_items=0` for every domain even though
each domain JSON contains all expected rows). This script walks the
results directory and emits a STATUS.md that always matches the data.

Truth sources:
  benchmarks/results_v0.4/<domain>.json     — per-item rows
  benchmarks/results_v0.4/judge_agreement.json — H9 metric block
  audit/v0.4/cost_ledger_merged.json        — Haiku cascade ledger
  audit/v0.4/integrity_probes_merged.jsonl  — integrity log file path

Run: python scripts/regenerate_results_status.py [--dry-run]
"""
from __future__ import annotations

import argparse
import datetime as dt
import json
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parents[1]
RESULTS = REPO / "benchmarks" / "results_v0.4"
AUDIT = REPO / "audit" / "v0.4"

DOMAINS = ["poetry_gen", "poetry_interp", "aut", "sci_creativity"]
EXPECTED_ARMS = (
    "haiku_bare",
    "haiku_cascade",
    "haiku_bare_2K_scorer",
    "haiku_generic_revise_2pass",
)


def _load_json(p: Path) -> dict:
    if not p.exists():
        return {}
    try:
        return json.loads(p.read_text(encoding="utf-8"))
    except json.JSONDecodeError as exc:
        print(f"WARN: cannot parse {p}: {exc}", file=sys.stderr)
        return {}


def domain_summary(domain: str) -> dict:
    data = _load_json(RESULTS / f"{domain}.json")
    rows = data.get("rows", {}) or {}
    items_with_rows = len(rows)
    complete_items = sum(
        1 for r in rows.values()
        if all(arm in r for arm in EXPECTED_ARMS)
    )
    return {
        "domain": domain,
        "items_with_rows": items_with_rows,
        "complete_items": complete_items,
    }


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--dry-run", action="store_true",
                    help="print to stdout instead of writing STATUS.md")
    args = ap.parse_args()

    rows = [domain_summary(d) for d in DOMAINS]
    ledger = _load_json(AUDIT / "cost_ledger_merged.json")
    judge_agreement = _load_json(RESULTS / "judge_agreement.json")
    cascade_total_usd = float(ledger.get("total_usd") or ledger.get("total_cost_usd") or 0.0)
    cascade_calls = int(ledger.get("n_calls") or ledger.get("total_calls") or 0)
    by_model = ledger.get("by_model") or {}
    haiku_model = next(iter(by_model.keys()), None) or "global.anthropic.claude-haiku-4-5-20251001-v1:0"

    ja = judge_agreement
    judge_total_usd = float(
        ja.get("total_cost_usd")
        or ja.get("judge_cost_usd")
        or (ja.get("audit") or {}).get("judge_cost_usd")
        or 0.0
    )
    judge_calls = int(
        ja.get("n")
        or ja.get("n_pairs")
        or ja.get("judge_calls")
        or (ja.get("audit") or {}).get("judge_calls")
        or 0
    )
    sonnet_model = (
        ja.get("model")
        or ja.get("judge_model")
        or (ja.get("audit") or {}).get("judge_model")
        or "global.anthropic.claude-sonnet-4-5-20250929-v1:0"
    )

    lines: list[str] = []
    a = lines.append
    a("# v0.4 Phase 7 (Bedrock pilot) — DONE\n")
    a(f"- Updated: {dt.datetime.now(dt.timezone.utc).isoformat()}")
    a(f"- Haiku cascade model: `{haiku_model}`")
    a(f"- Sonnet judge model:  `{sonnet_model}`")
    a("")
    a("This file is regenerated by `scripts/regenerate_results_status.py` from")
    a("the actual JSON files under `benchmarks/results_v0.4/` and")
    a("`audit/v0.4/cost_ledger_merged.json`. Edit those, not this.")
    a("")
    a("## Per-domain progress")
    a("")
    a("| domain | items_with_rows | complete_items (4/4 arms) |")
    a("|---|---|---|")
    for r in rows:
        a(f"| {r['domain']} | {r['items_with_rows']} | {r['complete_items']} |")
    a("")
    a("## Cost ledger (split honestly per v0.4.1 review)")
    a("")
    a(f"- Haiku cascade: **${cascade_total_usd:.2f}** over {cascade_calls} CLI calls "
      "(audit/v0.4/cost_ledger_merged.json)")
    a(f"- Sonnet judge:  **${judge_total_usd:.2f}** over {judge_calls} judge rows "
      "(benchmarks/results_v0.4/judge_agreement.json)")
    a(f"- Combined v0.4 pilot spend: **${cascade_total_usd + judge_total_usd:.2f}**")
    a("")
    a("## Phase outputs")
    a("")
    a("- merged_ledger: `audit/v0.4/cost_ledger_merged.json`")
    a("- merged_integrity_log: `audit/v0.4/integrity_probes_merged.jsonl`")
    a("- stats.json: `benchmarks/results_v0.4/stats.json`")
    a("- judge_agreement: `benchmarks/results_v0.4/judge_agreement.json`")
    a("")

    out = "\n".join(lines)
    target = RESULTS / "STATUS.md"
    if args.dry_run:
        print(out)
    else:
        target.write_text(out, encoding="utf-8")
        print(f"wrote {target}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
